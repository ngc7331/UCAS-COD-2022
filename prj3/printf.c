#include "printf.h"

#define UART_TX_FIFO		0x04
#define UART_STATUS		0x08
#define UART_TX_FIFO_FULL	(1 << 3)

volatile unsigned int *uart = (void *)0x60000000;

int
puts(const char *s)
{
	//TODO: Add your driver code here 
	int i = 0;
	volatile unsigned char *writeaddr = (void *) uart + UART_TX_FIFO;
	volatile unsigned int  *stataddr  = (void *) uart + UART_STATUS;
	while (s[i] != '\0') {
		while (*stataddr & UART_TX_FIFO_FULL) ;
		*writeaddr = s[i++];
	}
	return i;
}
